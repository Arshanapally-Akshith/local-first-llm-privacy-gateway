"""The dataset generator's entrypoint: instantiates every template
`_INSTANTIATIONS_PER_TEMPLATE` times and writes the result to
`benchmarks/data/dataset.jsonl`.

Run with:
    python -m benchmarks.generate.build_dataset

Regenerating must reproduce the committed file byte-for-byte, given the
same code and the same `DEFAULT_DATASET_SEED` — this is what "the
dataset can be regenerated byte-for-byte from the same commit" means
concretely, and it is what
`tests/unit/test_build_dataset.py::test_build_dataset_is_deterministic_across_independent_calls`
proves. Nothing in this module reads the clock, the filesystem (beyond
writing its own output), the network, or global `random` state.
"""

import json
import logging
import random
from pathlib import Path
from typing import Final

from benchmarks.generate.dataset_types import BenchmarkExample
from benchmarks.generate.inject import fill_template
from benchmarks.generate.seed import DEFAULT_DATASET_SEED
from benchmarks.generate.templates import TEMPLATES

_logger = logging.getLogger("benchmarks.generate")
_logger.setLevel(logging.INFO)
_logger.addHandler(logging.StreamHandler())
"""A plain stdlib logger, not the gateway's PII-safe one
(`src/core/logging.py`): that formatter is a security control for a
running gateway process handling real (if sanitized) traffic. Every
value this generator ever produces is synthetic by construction — the
PII-safety constraint that formatter enforces does not apply to
offline dataset-generation tooling, mirroring
`rehydration_fidelity/runner/run.py`'s identical choice."""

_INSTANTIATIONS_PER_TEMPLATE: Final[int] = 55
"""How many independently-sampled examples each template produces.
Guess, tuned only to land the total example count
(`len(TEMPLATES) * _INSTANTIATIONS_PER_TEMPLATE`) inside BUILD.md's
stated ~2-3k range for the current template count — not a measured
value, and safe to change; changing it changes every example past the
point the count shifts (see `_example_seed`'s docstring), which is
exactly why it is a named constant and not an inline literal."""

_DATASET_PATH: Final[Path] = Path(__file__).resolve().parent.parent / "data" / "dataset.jsonl"


def _example_seed(seed: int, counter: int) -> int:
    """Derive one example's own seed from the dataset-level `seed` and
    a running `counter`, by plain integer arithmetic — not
    `hash((seed, counter))`. `hash()` of an `int` is stable, but
    relying on that fact implicitly (rather than just not needing
    `hash()` at all) is exactly the kind of subtlety this task's
    determinism requirement should not depend on. `1_000_000` gives
    headroom for up to a million examples per base seed with no
    collision between two different `counter` values.
    """
    return seed * 1_000_000 + counter


def build_dataset(seed: int = DEFAULT_DATASET_SEED) -> tuple[BenchmarkExample, ...]:
    """Instantiate every template `_INSTANTIATIONS_PER_TEMPLATE` times
    and return the full, ordered example set.

    Each example gets its own freshly-constructed `random.Random`,
    seeded from `seed` and that example's position alone — not one
    `Random` instance shared and advanced across every example. A
    shared instance would make every example's generated values depend
    on exactly how many random draws every *earlier* example happened
    to make, so adding one more slot to one template would silently
    change every subsequent example's content — a fragile coupling to
    generator implementation details this design avoids entirely.

    Kept separate from `main()` so tests can call it directly without
    touching the filesystem, mirroring
    `rehydration_fidelity/runner/run.py::build_report()`'s identical
    split.
    """
    examples: list[BenchmarkExample] = []
    counter = 0
    for template in TEMPLATES:
        for _ in range(_INSTANTIATIONS_PER_TEMPLATE):
            example_rng = random.Random(_example_seed(seed, counter))
            example_id = f"ex-{counter:05d}"
            examples.append(fill_template(template, example_rng, example_id))
            counter += 1
    return tuple(examples)


def _to_json_dict(example: BenchmarkExample) -> dict[str, object]:
    return {
        "example_id": example.example_id,
        "template_id": example.template_id,
        "language": example.language,
        "text": example.text,
        "entities": [
            {
                "start": int(entity.start),
                "end": int(entity.end),
                "entity_type": entity.entity_type,
                "value": entity.value,
            }
            for entity in example.entities
        ],
    }


def to_jsonl(examples: tuple[BenchmarkExample, ...]) -> str:
    """Serialize `examples` to JSON Lines text, deterministically.

    `sort_keys=True` makes the byte-for-byte reproducibility guarantee
    independent of Python dict insertion order — belt-and-braces on top
    of `_to_json_dict()` already building its dict in a fixed field
    order, so a future refactor of that function cannot silently change
    the committed file's bytes without also changing its content.
    """
    lines = [
        json.dumps(_to_json_dict(example), ensure_ascii=False, sort_keys=True)
        for example in examples
    ]
    return "\n".join(lines) + "\n"


def write_jsonl(examples: tuple[BenchmarkExample, ...], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(to_jsonl(examples), encoding="utf-8")


def main() -> None:
    examples = build_dataset()
    write_jsonl(examples, _DATASET_PATH)
    _logger.info("wrote %d examples to %s", len(examples), _DATASET_PATH)


if __name__ == "__main__":
    main()
